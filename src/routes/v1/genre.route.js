const express = require('express');
const validate = require('../../middlewares/validate');
const genreValidation = require('../../validations/genre.validation');
const genreController = require('../../controllers/genre.controller');

const router = express.Router();

router
  .route('/')
  .post(validate(genreValidation.createGenre), genreController.createGenre)
  .get(validate(genreValidation.getGenres), genreController.getGenres);

router
  .route('/:genreId')
  .get(validate(genreValidation.getGenre), genreController.getGenre)
  .patch(validate(genreValidation.updateGenre), genreController.updateGenre)
  .delete(validate(genreValidation.deleteGenre), genreController.deleteGenre);

module.exports = router;

